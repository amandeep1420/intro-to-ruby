#Two common data structures that are used in Ruby are "arrays" and "hashes". 

//////////////////////////////////////
#ARRAYS

#An array is used to organize information into an ordered list. The list can have any data type (integers, strings, booleans, floats, etc.)
#Arrays are denoted using square brackets []. Arrays are made up of elements, which are the individual values you're storing.
#Ex. [1, 2, 3, 4, "cat", true]

#You can use the index to access each element of an array; indices start at [0] for the first element.
#Ex. [1, 2, 3, 4][0] = 1

//////////////////////////////////////
#HASHES


#A hash (also known as a dictionary) is a set of key-value pairs. Hashes are denoted using curly braces {}. 
#Use => (rocket) to assign a value to a key. Ex. key => value
#Keys in a hash are usually represented as symbols.

#Example hash: {:dog => 'barks', cat => 'meows', :pig => 'oinks'}[:cat] will return "meows"

