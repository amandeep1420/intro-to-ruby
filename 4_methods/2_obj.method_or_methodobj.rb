There are two ways to call methods that will be discussed.

1. The some_method(obj) format is when you send arguments to a method call; in this example, obj is the argument being pass in to the 
some_method method. 

2. Sometimes, you will see methods called with an -explicit caller-, like this ---> a_caller.some_method(obj) 
For now, it's best to think of that code as some_method modifying a_caller. 

Memorize these formats/keep them in mind - you've encountered them both already.